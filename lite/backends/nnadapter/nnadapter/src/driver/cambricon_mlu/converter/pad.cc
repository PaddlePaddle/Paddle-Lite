#include "operation/pad.h"
#include "driver/cambricon_mlu/converter.h"
#include "utility/debug.h"
#include "utility/logging.h"

namespace nnadapter {
namespace cambricon_mlu {

int ConvertPad(Converter* converter, core::Operation* operation) {
  PAD_OPERATION_EXTRACT_INPUTS_OUTPUTS

  // Convert to magicmind tensors and node
  auto input_tensor = converter->GetMappedTensor(input_operand);
  if (!input_tensor) {
    input_tensor = converter->ConvertOperand(input_operand);
  }
  auto paddings_tensor = converter->GetMappedTensor(pads_operand);
  if (!paddings_tensor) {
    paddings_tensor = converter->ConvertOperand(pads_operand);
  }
  auto value_tensor = converter->ConvertOperand(value_operand);
  auto pad_node = converter->network()->AddIPadNode(
      input_tensor, paddings_tensor, value_tensor);
  NNADAPTER_CHECK(pad_node) << "Failed to add pad node.";
  auto output_tensor = pad_node->GetOutput(0);
  converter->UpdateTensorMap(output_operand, output_tensor);
  return NNADAPTER_NO_ERROR;
}

}  // namespace cambricon_mlu
}  // namespace nnadapter
